    private void cargarBancos() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        
        for (Bancos banco : BancosController.getAllBancos()) {
            model.addElement(banco);
        }
        
        jComboBoxBanco.setModel(model);
    }


